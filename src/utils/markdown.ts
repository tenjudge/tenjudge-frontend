// Placeholder format uses only alphanumeric characters to avoid triggering
// markdown-it emphasis/escape rules. Curly braces are safe literal text.
const placeholderRe = /\{KTX(\d+)\}/g

function renderMath(
  text: string,
  mathBlocks: string[],
): string {
  // Block math: \[...\] — used by many LLMs
  text = text.replace(/\\\[([\s\S]*?)\\\]/g, (_full, math: string) => {
    const id = mathBlocks.length
    mathBlocks.push(
      katex.renderToString(math.trim(), {
        displayMode: true,
        throwOnError: false,
      }),
    )
    return `{KTX${id}}`
  })

  // Inline math: \(...\)
  text = text.replace(/\\\(([^\n]*?)\\\)/g, (_full, math: string) => {
    const id = mathBlocks.length
    mathBlocks.push(
      katex.renderToString(math, {
        displayMode: false,
        throwOnError: false,
      }),
    )
    return `{KTX${id}}`
  })

  // Block math: $$...$$
  text = text.replace(/\$\$([\s\S]*?)\$\$/g, (_full, math: string) => {
    const id = mathBlocks.length
    mathBlocks.push(
      katex.renderToString(math.trim(), {
        displayMode: true,
        throwOnError: false,
      }),
    )
    return `{KTX${id}}`
  })

  // Inline math: $...$ — single-line, not preceded by \
  text = text.replace(/(?<!\\)\$([^\n$]+?)(?<!\\)\$/g, (_full, math: string) => {
    const id = mathBlocks.length
    mathBlocks.push(
      katex.renderToString(math, {
        displayMode: false,
        throwOnError: false,
      }),
    )
    return `{KTX${id}}`
  })

  return text
}

export function renderMarkdown(source: string): string {
  const mathBlocks: string[] = []

  // Split by fenced code blocks so math delimiters inside code are ignored.
  const parts = source.split(/(```[\s\S]*?```)/g)
  const processed = parts
    .map((part, i) => {
      // Odd indices are fenced code blocks — leave untouched.
      if (i % 2 === 1) return part
      return renderMath(part, mathBlocks)
    })
    .join('')

  let html = markdown.render(processed)

  html = DOMPurify.sanitize(html, {
    ADD_TAGS: MATHML_TAGS,
    ADD_ATTR: [...MATHML_ATTR, 'class', 'type', 'aria-label', 'data-code', 'viewBox', 'focusable'],
  })

  // Swap KaTeX HTML back after sanitisation so it renders as real elements.
  html = html.replace(placeholderRe, (_full, id: string) => {
    return mathBlocks[Number(id)] ?? ''
  })

  return html
}

export function renderMarkdownPlain(source: string): string {
  const mathBlocks: string[] = []

  const parts = source.split(/(```[\s\S]*?```)/g)
  const processed = parts
    .map((part, i) => {
      if (i % 2 === 1) return part
      return renderMath(part, mathBlocks)
    })
    .join('')

  let html = plainMarkdown.render(processed)

  html = DOMPurify.sanitize(html, {
    ADD_TAGS: MATHML_TAGS,
    ADD_ATTR: [...MATHML_ATTR, 'class', 'type'],
  })

  html = html.replace(placeholderRe, (_full, id: string) => {
    return mathBlocks[Number(id)] ?? ''
  })

  return html
}
