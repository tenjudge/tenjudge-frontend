function escapeHtml(source: string): string {
  return source
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
}

function escapeAttribute(source: string): string {
  return escapeHtml(source).replace(/'/g, '&#39;')
}

function highlightCode(code: string, language: string): string {
  const normalizedLanguage = language.trim().toLowerCase()

  if (normalizedLanguage && hljs.getLanguage(normalizedLanguage)) {
    try {
      return hljs.highlight(code, {
        language: normalizedLanguage,
        ignoreIllegals: true,
      }).value
    } catch {
      return escapeHtml(code)
    }
  }

  try {
    return hljs.highlightAuto(code).value
  } catch {
    return escapeHtml(code)
  }
}

function formatLanguageLabel(language: string): string {
  const normalized = language.trim().toLowerCase()
  if (!normalized) return 'Code'
  return LANGUAGE_LABELS[normalized] ?? normalized.toUpperCase()
}

const plainMarkdown = new MarkdownIt({
  html: false,
  linkify: true,
  breaks: true,
})

const markdown = new MarkdownIt({
  html: false,
  linkify: true,
  breaks: true,
})

markdown.renderer.rules.fence = (tokens, index): string => {
  const token = tokens[index]
  if (!token) return ''

  const info = token.info ? token.info.trim().split(/\s+/)[0] ?? '' : ''
  const highlighted = highlightCode(token.content, info)
  const safeLanguage = escapeHtml(info)
  const languageClass = safeLanguage ? ` language-${safeLanguage}` : ''
  const languageLabel = escapeHtml(formatLanguageLabel(info))
  const copyCode = escapeAttribute(token.content)

  return [
    '<div class="markdown-code-block">',
    '<div class="markdown-code-block__header">',
    `<span class="markdown-code-block__title">${languageLabel}</span>`,
    `<button class="markdown-code-block__copy" type="button" aria-label="Copy code" data-code="${copyCode}">`,
    '<svg viewBox="0 0 24 24" aria-hidden="true" focusable="false">',
    '<rect x="8" y="8" width="10" height="12" rx="2"></rect>',
    '<path d="M6 16H5a2 2 0 0 1-2-2V6a2 2 0 0 1 2-2h8a2 2 0 0 1 2 2v1"></path>',
    '</svg>',
    '</button>',
    '</div>',
    `<pre><code class="hljs${languageClass}">${highlighted}</code></pre>`,
    '</div>',
  ].join('')
}

